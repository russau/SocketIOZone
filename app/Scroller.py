class Scroller(object):

    def __init__(self, emitter):
        self.emitter = emitter

    def scrollText(self, s, r=0xe4, g=0xde, b=0x00):
        print("Scrolling %s" % s)
        width = 6 * len(s) + 16
        height = 8
        board = []

        for y in range(height):
            #  add 'blankness' before the 'text'
            row = []
            for x in range(width):
                color = {'r':0x00, 'g':0x00, 'b':0x00}
                row.append(color)
            board.append(row)

        # based on https://github.com/adafruit/Adafruit-GFX-Library/blob/0acee33cfe8b500c5a2a1c4f29913746936b53ba/Adafruit_GFX.cpp#L437
        x = 16
        y = 0

        for ch in s:
            c = ord(ch)
            if c > 0 and c < 256:
                for i in range(6):
                    line = font[(c * 5) + i]
                    if i == 5: line = 0x0

                    for j in range(8):
                        if line & 0x01 == 1:
                            # drawPixel(x+i, y+j, color);
                            if y+j < height and x+i < width:
                                board[y+j][x+i] = {'r':r, 'g':g, 'b':b}
                        line >>= 1
                x += 6

        # loop thru the width of the text
        for _ in range(width):
            # a pop one pixel of the 'y' for each row
            for y in range(height):
                board[y].pop(0)
            squares = boardToLights(board)
            self.emitter(squares)
            time.sleep(0.1)


def boardToLights(board):
    array_pos = -1
    width = len(board[0])
    height = len(board)
    squares = [{'r':0, 'g':0, 'b':0}] * 8 * 16

    for y in range(min(height, 8)):
        for x in range(min(width, 16)):
            if x < 8:
                array_pos = y * 8 + x
                squares[array_pos] = board[y][x]
            elif x < 16:
                array_pos = y * 8 + x-8 + 64
                squares[array_pos] = board[y][x]

    return squares

